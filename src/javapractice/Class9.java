package javapractice;

public class Class9 {

	public static void main(String[] args) {
		//Swapping two numbers
		int a=10, b=20;
		System.out.println("Before swapping numbers are --"+a+"  "+b);
		
		int t=a;
		a=b;
		b=t;
		
		System.out.println("After swapping numbers are--"+a+ "  "+b);
		

	}

}
